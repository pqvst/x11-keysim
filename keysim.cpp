void press(int ch)
{
	// Lookup keysym for unicode character
	int keysym = 0;
	if (ch < XLookupLen)
		keysym = XLookup[ch];
	if (keysym == 0) {
		std::cout << "could not find: " << ch << " in lookup table >.<" << std::endl;
		return;
	} else {
		std::cout << "character: " << ch << " -> keysym: " << keysym << std::endl;
	}

	// Open X display
	Display *display = XOpenDisplay(0);
	if (!display) {
		std::cout << "could not open X display" << std::endl;
		return;
	}

	// Remap some high unusued keycode to the keysym that we want
	int first_keycode = 999;
	int keysyms_per_keycode = 1;
	KeySym keysyms[] = { keysym };
	int num_codes = 1;
	XChangeKeyboardMapping(display, first_keycode, keysyms_per_keycode, keysyms, num_codes);

	// Simulate the key press events
	XTestFakeKeyEvent(display, first_keycode, True, 0);
	XFlush(display);
	XTestFakeKeyEvent(display, first_keycode, False, 0);
	XFlush(display);

	// Close X display
	XCloseDisplay(display);
}

int parseHex(char* str)
{
	int x;
	std::stringstream ss;
	ss << std::hex << str;
	ss >> x;
	return x;
}

int parseInt(char* str)
{
	int x;
	std::stringstream ss;
	ss << str;
	ss >> x;
	return x;
}

int main(int argc, char* argv[])
{
	int chr = 0;
	int time = 0;

	if (argc > 1) {
		chr = parseHex(argv[1]);
	} else {
		std::cout << "you must specify a character in hex format (e.g. 0x00d6)" << std::endl;
		return 0;
	}

	for (int i = 2; i < argc; i++) {
		bool more = i < argc - 1;
		std::string arg = argv[i];
		if (arg == "--sleep" && more)
			time = parseInt(argv[++i]);
	}

	if (time > 0) {
		std::cout << "waiting " << time << " seconds..." << std::endl;
		sleep((unsigned int)time);
	}

	press(chr);
	return 0;
}

